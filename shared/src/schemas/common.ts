import { z } from 'zod';

// ============================================================
// Общие валидаторы, переиспользуемые несколькими схемами.
// ============================================================

/** ИНН: 10 цифр (юрлицо) или 12 (ИП/физлицо). Контрольная сумма не проверяется. */
export const INN_RE = /^\d{10}(\d{2})?$/;

/** Нормализация ИНН из формы: trim, пустая строка → null. */
const normalizeInn = (v: unknown): unknown => {
  if (typeof v !== 'string') return v;
  const t = v.trim();
  return t === '' ? null : t;
};

/**
 * Строгий ИНН — для НОВЫХ записей: нормализованный + формат 10/12 цифр.
 * Значение опционально (может отсутствовать/быть null).
 */
export const innStrictSchema = z.preprocess(
  normalizeInn,
  z.string().regex(INN_RE, 'ИНН 10 или 12 цифр').nullable(),
);

/**
 * Мягкий ИНН — для ОБНОВЛЕНИЯ существующих записей. Часть ранее заведённых контрагентов
 * имеет унаследованные «грязные» ИНН (11 цифр, буквы и т.п.); навязывать им формат при
 * правке ДРУГИХ полей нельзя — иначе запись нельзя будет сохранить. Пустое → null,
 * ограничиваем лишь длину. Формат для нового ввода проверяет клиент (не трогая старое).
 */
export const innLenientSchema = z.preprocess(
  normalizeInn,
  z.string().max(20).nullable(),
);
