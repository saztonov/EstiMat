// Остаток по строке материала: сколько ещё можно заявить.
//
// База для колонки «Остаток» и для набора долей («100% остатка»). Отрицательным не бывает:
// заявить сверх сметы можно (это отражается тегом «Сверх сметы»), но тогда остатка просто нет.

/** Допуск на плавающую точку: 134.244 − 134.244 не должно дать 2.8e-14 вместо нуля. */
const EPS = 1e-6;

/** Остаток = кол-во по смете − уже заявлено; ≤ EPS → 0. */
export function remainingOf(quantity: number, ordered: number): number {
  const left = quantity - ordered;
  return left > EPS ? left : 0;
}
