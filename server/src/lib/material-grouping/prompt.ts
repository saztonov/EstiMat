/**
 * Промпты умной группировки.
 *
 * Версия входит в хэш входа: правка текста обязана инвалидировать кэш готовых результатов.
 *
 * Промпт намеренно короткий (~700 токенов): он платится на КАЖДОМ батче (их до 20), а на
 * LM Studio ещё и давит контекст, провоцируя переполнение рассуждением. Из ТЗ выброшен список
 * из 30+ характеристик (химсовместимость, IP, морозостойкость, давление, производитель/серия):
 * в БД у материала есть только название и единица (material_catalog.attributes пуст у всех
 * 1185 позиций), поэтому спрашивать про IP — это прямая инструкция выдумывать.
 *
 * SMETCHIK_ROLE из extract-порта не переиспользуется: он на 80 % про мусор OCR (оси, штампы,
 * экспликации), а здесь на входе строки БД — эти запреты были бы шумом.
 */
import type { GroupingBatch, GroupingLine, GroupingSettings, DraftGroup } from './types.js';

export const PROMPT_VERSION = 'mg-1';

const BASE = `Ты — ведущий инженер-сметчик с 20-летним опытом. Группируешь материалы сметы по реальным производственным операциям.

ГРУППА — это ОДНО из: строительная операция; конструктивный элемент; монтажный узел; слой конструкции; повторяемый комплект.
Образцы названий: «Армирование плиты перекрытия», «Бетонирование стен», «Кладка перегородок из блоков», «Устройство кровельного пирога», «Монтаж противопожарной двери», «Монтаж трубопровода», «Насосный узел», «Монтаж кабельной линии», «Устройство стяжки пола».
Название группы — операция, а не категория материала («Трубы» — плохо, «Монтаж трубопровода» — хорошо).

ДАННЫЕ. О материале известны ТОЛЬКО наименование, единица и количество. Характеристик, паспортов, норм и цен НЕТ и не будет.
Опирайся ИСКЛЮЧИТЕЛЬНО на то, что явно написано в наименовании: диаметр (Ду/DN/Ø), размер, тип соединения, резьба (ВР/НР), материал основы.
НЕ додумывай: давление, температуру, IP, морозостойкость, химическую совместимость, производителя, серию, модель, проектные решения.

ЗАПРЕЩЕНО:
- использовать номера (idx) вне входного списка; каждый idx — максимум в одной группе;
- менять количества и единицы измерения;
- ссылаться на ГОСТ/СП/нормы, которых нет во входных данных;
- приписывать материалу характеристики, отсутствующие в наименовании;
- возвращать рассуждения.

ОЦЕНКА группы — две НЕЗАВИСИМЫЕ характеристики:
completeness: complete — состава хватает для операции; incomplete — явно не хватает обязательного элемента; unknown — данных недостаточно для вывода.
compatibility: no_issues — противоречий не видно; possible_issue — из наименований видно возможное несоответствие (разные диаметры в одном узле, несовместимая резьба, разный материал основы); unknown — данных недостаточно.
Не уверен — ставь unknown. Это честный ответ, а не ошибка.
missing — РЕКОМЕНДАЦИЯ сметчику проверить, а не заявка на закупку.

Строка не относится ни к одной операции → ungrouped. Расходник, нужный нескольким операциям → shared.

Ответ — ТОЛЬКО JSON:
{"groups":[{"name":"Армирование плиты перекрытия","purpose":"Устройство армокаркаса","completeness":"unknown","compatibility":"no_issues","idx":[1,5,9],"issues":[{"severity":"review","message":"...","idx":[5]}],"missing":[{"name":"Проволока вязальная","reason":"...","need":"recommended"}]}],"shared":[3],"ungrouped":[7]}
severity: warning|review|recommendation. need: required|conditional|recommended.`;

// Выключенный параметр не должен ни разделять материалы, ни считаться ошибкой. Самая надёжная
// защита — не показывать его модели вовсе (см. buildBatchUserPrompt), плюс явный запрет здесь.
function settingsNote(s: GroupingSettings): string {
  const notes: string[] = [];
  notes.push(
    s.costType
      ? 'Материалы разных видов работ объединять НЕЛЬЗЯ.'
      : 'Вид работ НЕ является основанием для разделения: материалы разных видов работ можно объединять, если они относятся к одной операции. Различие вида работ — не ошибка и не повод для issues.',
  );
  notes.push(
    s.location
      ? 'Материалы разных местоположений объединять НЕЛЬЗЯ.'
      : 'Местоположение НЕ является основанием для разделения. Различие местоположений — не ошибка.',
  );
  notes.push(
    s.locationType
      ? 'Материалы разных типов работ объединять НЕЛЬЗЯ.'
      : 'Тип работы (РП-1, МОП, ТПУ и т. п.) НЕ является основанием для разделения. Различие типов — не ошибка.',
  );
  return `ПРАВИЛА ГРАНИЦ:\n${notes.map((n) => `- ${n}`).join('\n')}`;
}

export function buildSystemPrompt(s: GroupingSettings): string {
  return `${BASE}\n\n${settingsNote(s)}`;
}

/** Строка для модели: idx + всё, что разрешено настройками. */
function lineText(l: GroupingLine, idx: number, s: GroupingSettings): string {
  const parts = [`${idx}|${l.name}|${l.unit}|${round(l.quantity)}`];
  if (s.costType && l.costTypeName) parts.push(`вид работ: ${l.costTypeName}`);
  if (s.location && l.locationLabels.length) parts.push(`место: ${l.locationLabels.join(', ')}`);
  if (s.locationType && l.typeLabels.length) parts.push(`тип: ${l.typeLabels.join(', ')}`);
  if (l.materialGroupName) parts.push(`раздел: ${l.materialGroupName}`);
  if (l.workNames.length) parts.push(`работы: ${l.workNames.slice(0, 3).join('; ')}`);
  return parts.join(' · ');
}

const round = (n: number) => Math.round(n * 1e4) / 1e4;

export function buildBatchUserPrompt(batch: GroupingBatch, s: GroupingSettings): string {
  const lines = batch.lines.map((l, i) => lineText(l, i + 1, s)).join('\n');
  return `Материалы (формат: idx|наименование|ед|количество · контекст):\n${lines}\n\nСгруппируй по производственным операциям. Верни только JSON.`;
}

/**
 * Второй проход: слить черновые группы одной партиции, разъехавшиеся по батчам. Модель видит
 * только карточки групп (id, назначение, состав) — строки сюда не передаются, иначе на 576
 * позициях промпт reduce был бы больше, чем весь map.
 */
export function buildMergeUserPrompt(groups: DraftGroup[]): string {
  const cards = groups
    .map((g) => {
      const sample = g.orderKeys.length;
      return `${g.id}|${g.name}|${g.purpose ?? ''}|позиций: ${sample}`;
    })
    .join('\n');
  return `Черновые группы одной области (формат: id|название|назначение|число позиций):\n${cards}\n\nОбъедини группы, которые описывают ОДНУ И ТУ ЖЕ производственную операцию (их разнесло по разным наборам). Не объединяй разные операции. Верни только JSON:\n{"merge":[{"into":"<id>","from":["<id>","<id>"],"name":"итоговое название"}]}\nЕсли объединять нечего — {"merge":[]}.`;
}

export const MERGE_SYSTEM_PROMPT = `Ты — ведущий инженер-сметчик. Тебе дают карточки черновых групп материалов одной области сметы. Часть групп описывает одну и ту же производственную операцию, потому что материалы обрабатывались разными наборами.
Объединяй ТОЛЬКО группы одной и той же операции. Разные операции (например «Монтаж трубопровода» и «Изоляция трубопровода») объединять НЕЛЬЗЯ.
Не придумывай новые id. Не возвращай рассуждения. Ответ — только JSON.`;
