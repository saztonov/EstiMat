// ── Резолверы (без кэша, по образцу resolveModelSetting) ──────────────────────

function overrideOf(raw: unknown, id: AiPromptId): string | null {
  if (!raw || typeof raw !== 'object' || Array.isArray(raw)) return null;
  const v = (raw as Record<string, unknown>)[id];
  return typeof v === 'string' && v.trim() ? v : null;
}

/** Действующий текст одного промпта: переопределение из БД или дефолт. */
export async function resolvePrompt(pool: Pool, id: AiPromptId): Promise<string> {
  const { rows } = await pool.query(`SELECT value FROM app_settings WHERE key = 'ai_prompts'`);
  return overrideOf(rows[0]?.value, id) ?? PROMPT_DEFAULTS[id];
}

/** Все действующие тексты (для GET и для снимка задания группировки). */
export async function resolveAllPrompts(pool: Pool): Promise<Record<AiPromptId, string>> {
  const { rows } = await pool.query(`SELECT value FROM app_settings WHERE key = 'ai_prompts'`);
  const raw = rows[0]?.value;
  const out = {} as Record<AiPromptId, string>;
  for (const id of AI_PROMPT_IDS) out[id] = overrideOf(raw, id) ?? PROMPT_DEFAULTS[id];
  return out;
}
