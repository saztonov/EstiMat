-- 0077: комментарий снабжения к заказу поставщику.
--
-- Почему на заказе, а не на предложении: это заметка о заказе в целом («ждём подтверждения по
-- срокам», «поставщик просит предоплату»), и она обязана пережить любое предложение — поставщики
-- добавляются и убираются, а заметка остаётся.
--
-- Почему НЕ участвует в row_version: комментарий не входит в закупочный контракт (состав, график,
-- цены, поставщик). Если бы его правка бампала версию, у пользователя с открытой формой цен или
-- графика ломался бы expectedVersion — правка соседа отменяла бы его работу без всякой причины.
-- Поэтому роут комментария меняет только updated_at.
--
-- Аддитивная и идемпотентная (совместима с deploy-estimat --migrate: один батч, чистый SQL).

ALTER TABLE supplier_orders ADD COLUMN IF NOT EXISTS comment TEXT;
