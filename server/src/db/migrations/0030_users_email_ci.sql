-- 0030: регистронезависимый (и trim-устойчивый) email пользователей.
-- Канон нормализации всего приложения — lower(btrim(email)). Приводим существующие email
-- к канону и защищаем от дубликатов уникальным функциональным индексом. Идемпотентно, чистый SQL.

-- 1. Нормализация существующих значений (только реально отличающиеся — минимум записи).
UPDATE users SET email = lower(btrim(email)) WHERE email <> lower(btrim(email));

-- 2. Функциональный уникальный индекс по канону — главный CI-инвариант уникальности email
--    (существующий UNIQUE(email) остаётся как редундантная защита; удалять не обязательно).
CREATE UNIQUE INDEX IF NOT EXISTS users_email_norm_uidx ON users (lower(btrim(email)));
